        	<h1>The Features of Elegy</h1>
        	<p>Elegy is the only app that offers day-of funeral planning and organization. Create a central source of information for all the Guests you invite to help remember and celebrate your lost one.</p>

          <h2>Memories</h2>
          <p>Never forget a thing - add your own memories, pictures and videos in the feed. Any Guests you invite can do the same, thus creating a whole wall of thoughts, memories and happy things for your lost one.</p>

          <h2>Schedule</h2>
          <p>There's an interactive schedule built in to include times and locations of different parts of the day. Let all your Guests know where and when the Wake, Ceremony, Burial or Reception is and instantly notify them if anything changes.</p>

          <h2>Program</h2>
          <p>Create a virtual program for all Guests to view. Include anything a regular paper program would have, plus it's better for the environment!</p>

          <h2>Journal</h2>
          <p>Write down thoughts whenever the moment strikes.</p>
